valor1 = int(input("Digite o valor 1: "))  # Solicita ao usuário que digite o primeiro valor e o converte para inteiro
valor2 = int(input("Digite o valor 2: "))  # Solicita ao usuário que digite o segundo valor e o converte para inteiro

soma = valor1 + valor2  # Calcula a soma dos dois valores

print(f"O valor da soma é: {soma}")  # Exibe o resultado da soma usando uma f-string para formatar a mensagem
